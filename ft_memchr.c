#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*src;

	i = 0;
	src = (unsigned char *)s;
	if (!s)
		return (NULL);
	while (i < n && src[i])
		if (src[i] == c)
			return (&src[i]);
	return (NULL);
}
